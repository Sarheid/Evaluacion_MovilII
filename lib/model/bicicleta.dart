import 'package:flutter/cupertino.dart';

class Bicicleta {
  final int id;
  final String name;
  final String descripcion;
  final String precio;
  final String velocidades;
  final String color;

  Bicicleta({
    required this.id,
    required this.name,
    required this.descripcion,
    required this.precio,
    required this.velocidades,
    required this.color,
  });
}

